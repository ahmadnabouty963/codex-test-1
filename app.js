const defaultGoals = { calories: 2200, protein: 150, carbs: 230, fat: 70, sugar: 50, water: 2.5 };
let state = {
  date: toDateKey(new Date()),
  lang: localStorage.getItem("nourish-language") || "de",
  theme: localStorage.getItem("nourish-theme") || "light",
  filter: "all",
  goals: JSON.parse(localStorage.getItem("nourish-goals")) || defaultGoals,
  days: JSON.parse(localStorage.getItem("nourish-days")) || {}
};

const $ = (selector) => document.querySelector(selector);
const $$ = (selector) => [...document.querySelectorAll(selector)];
const t = (key) => translations[state.lang][key] || translations.de[key] || key;
const todayKey = () => toDateKey(new Date());

function toDateKey(date) {
  const year = date.getFullYear();
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
}

function parseDate(key) {
  const [year, month, day] = key.split("-").map(Number);
  return new Date(year, month - 1, day);
}

function currentDay() {
  if (!state.days[state.date]) state.days[state.date] = { foods: [], water: 0 };
  return state.days[state.date];
}

function saveDays() {
  localStorage.setItem("nourish-days", JSON.stringify(state.days));
}

function formatNumber(value, digits = 0) {
  return new Intl.NumberFormat(state.lang === "ar" ? "ar-EG" : state.lang, { maximumFractionDigits: digits }).format(value || 0);
}

function updateLanguage() {
  document.documentElement.lang = state.lang;
  document.documentElement.dir = state.lang === "ar" ? "rtl" : "ltr";
  $$('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  $("#languageSelect").value = state.lang;
  localStorage.setItem("nourish-language", state.lang);
  render();
}

function updateTheme() {
  document.documentElement.dataset.theme = state.theme;
  $("#themeToggle").textContent = state.theme === "dark" ? "☀" : "☾";
  localStorage.setItem("nourish-theme", state.theme);
}

function totals(status) {
  const foods = currentDay().foods.filter((food) => !status || food.status === status);
  return foods.reduce((sum, food) => {
    ["calories", "protein", "carbs", "fat", "sugar", "fiber", "salt"].forEach((key) => sum[key] += Number(food[key]) || 0);
    return sum;
  }, { calories: 0, protein: 0, carbs: 0, fat: 0, sugar: 0, fiber: 0, salt: 0 });
}

function render() {
  renderDate();
  renderSummary();
  renderMeals();
  renderWeeklyChart();
  renderStreak();
}

function renderDate() {
  const date = parseDate(state.date);
  const diff = Math.round((date - parseDate(todayKey())) / 86400000);
  $("#dateLabel").textContent = diff === 0 ? t("today") : diff === -1 ? t("yesterday") : diff === 1 ? t("tomorrow") : new Intl.DateTimeFormat(state.lang, { weekday: "long" }).format(date);
  $("#dateFull").textContent = new Intl.DateTimeFormat(state.lang, { day: "2-digit", month: "long", year: "numeric" }).format(date);
  $("#datePicker").value = state.date;
}

function renderSummary() {
  const eaten = totals("eaten");
  const planned = totals("planned");
  const remaining = Math.max(state.goals.calories - eaten.calories, 0);
  const progress = Math.min(eaten.calories / state.goals.calories, 1) * 360;
  $("#calorieRing").style.setProperty("--progress", `${progress}deg`);
  $("#remainingCalories").textContent = formatNumber(remaining);
  $("#consumedCalories").textContent = `${formatNumber(eaten.calories)} ${t("kcal")}`;
  $("#plannedCalories").textContent = `${formatNumber(planned.calories)} ${t("kcal")}`;
  $("#goalCalories").textContent = `${formatNumber(state.goals.calories)} ${t("kcal")}`;

  const macros = [
    { key: "protein", label: t("protein"), color: "var(--primary)" },
    { key: "carbs", label: t("carbs"), color: "var(--amber)" },
    { key: "fat", label: t("fat"), color: "var(--blue)" },
    { key: "sugar", label: t("sugar"), color: "var(--danger)" }
  ];
  $("#macroList").innerHTML = macros.map(({ key, label, color }) => {
    const percent = Math.min((eaten[key] / state.goals[key]) * 100, 100) || 0;
    return `<div class="macro-item"><div class="macro-top"><strong>${label}</strong><span>${formatNumber(eaten[key], 1)} / ${formatNumber(state.goals[key])} g</span></div><div class="progress-track"><div class="progress-fill" style="width:${percent}%;background:${color}"></div></div></div>`;
  }).join("");

  const water = currentDay().water || 0;
  $("#waterAmount").textContent = formatNumber(water, 2);
  $("#waterGoal").textContent = formatNumber(state.goals.water, 1);
  $("#waterProgress").style.width = `${Math.min(water / state.goals.water * 100, 100)}%`;
}

function renderMeals() {
  const allFoods = currentDay().foods;
  $("#mealGroups").innerHTML = Object.entries(mealMeta).map(([meal, meta]) => {
    const mealFoods = allFoods.filter((food) => food.meal === meal && (state.filter === "all" || food.status === state.filter));
    const mealCalories = mealFoods.reduce((sum, food) => sum + Number(food.calories || 0), 0);
    const rows = mealFoods.length ? mealFoods.map(food => `
      <div class="food-row">
        <div class="food-name"><strong>${escapeHtml(food.name)}</strong><small>${escapeHtml(food.portion || "–")} · P ${formatNumber(food.protein,1)}g · C ${formatNumber(food.carbs,1)}g · F ${formatNumber(food.fat,1)}g</small></div>
        <span class="status-pill ${food.status}">${t(food.status)}</span>
        <div class="food-energy"><strong>${formatNumber(food.calories)} ${t("kcal")}</strong><small>${formatNumber(food.sugar,1)}g ${t("sugar").split(" ")[0]}</small></div>
        <div class="row-actions"><button class="row-action edit-food" data-id="${food.id}" aria-label="Edit">✎</button><button class="row-action delete delete-food" data-id="${food.id}" aria-label="Delete">×</button></div>
      </div>`).join("") : `<div class="empty-meal"><span>${t("noEntries")}</span><button class="text-add add-to-meal" data-meal="${meal}">＋ ${t("add")}</button></div>`;
    return `<article class="meal-card"><header class="meal-header"><div class="meal-title"><span class="meal-emoji">${meta.emoji}</span><div><h3>${t(meta.key)}</h3><small>${mealFoods.length} ${mealFoods.length === 1 ? "Entry" : "Entries"}</small></div></div><div class="meal-total"><strong>${formatNumber(mealCalories)}</strong> ${t("kcal")}</div></header>${rows}</article>`;
  }).join("");
}

function renderWeeklyChart() {
  const selected = parseDate(state.date);
  const days = Array.from({ length: 7 }, (_, index) => {
    const date = new Date(selected); date.setDate(selected.getDate() - 6 + index);
    const key = toDateKey(date);
    const calories = (state.days[key]?.foods || []).filter(f => f.status === "eaten").reduce((sum, f) => sum + Number(f.calories || 0), 0);
    return { date, key, calories };
  });
  const max = Math.max(state.goals.calories, ...days.map(day => day.calories));
  const average = days.reduce((sum, day) => sum + day.calories, 0) / 7;
  $("#weeklyAverage").textContent = `${formatNumber(average)} ${t("kcal")}`;
  $("#weeklyChart").innerHTML = days.map(day => `<div class="chart-day ${day.key === todayKey() ? "today" : ""}"><strong>${formatNumber(day.calories)}</strong><div class="bar-shell"><span class="bar-fill" style="height:${day.calories / max * 100}%"></span></div><small>${new Intl.DateTimeFormat(state.lang, { weekday: "short" }).format(day.date)}</small></div>`).join("");
}

function renderStreak() {
  let streak = 0;
  const cursor = parseDate(todayKey());
  while (true) {
    const day = state.days[toDateKey(cursor)];
    if (!day || !day.foods?.some(f => f.status === "eaten")) break;
    streak += 1; cursor.setDate(cursor.getDate() - 1);
  }
  $("#streakDays").textContent = formatNumber(streak);
}

function openFoodDialog(meal = "breakfast", food = null) {
  $("#foodForm").reset();
  $("#foodId").value = food?.id || "";
  $("#foodMeal").value = food?.meal || meal;
  $("#foodStatus").value = food?.status || "eaten";
  ["name", "portion", "calories", "protein", "carbs", "fat", "sugar", "fiber", "salt"].forEach(key => {
    $(`#food${key[0].toUpperCase()}${key.slice(1)}`).value = food?.[key] ?? "";
  });
  $("#foodDialogTitle").textContent = food ? t("editFoodTitle") : t("addFoodTitle");
  $("#foodDialog").showModal();
  setTimeout(() => $("#foodName").focus(), 50);
}

function submitFood(event) {
  event.preventDefault();
  const id = $("#foodId").value || crypto.randomUUID();
  const food = { id, name: $("#foodName").value.trim(), meal: $("#foodMeal").value, status: $("#foodStatus").value, portion: $("#foodPortion").value.trim() };
  ["calories", "protein", "carbs", "fat", "sugar", "fiber", "salt"].forEach(key => food[key] = Number($(`#food${key[0].toUpperCase()}${key.slice(1)}`).value) || 0);
  const index = currentDay().foods.findIndex(item => item.id === id);
  if (index >= 0) currentDay().foods[index] = food; else currentDay().foods.push(food);
  saveDays(); $("#foodDialog").close(); render(); showToast(t("saved"));
}

function openGoals() {
  Object.entries({ CaloriesInput: "calories", ProteinInput: "protein", CarbsInput: "carbs", FatInput: "fat", SugarInput: "sugar", WaterInput: "water" }).forEach(([id, key]) => $(`#goal${id}`).value = state.goals[key]);
  $("#goalsDialog").showModal();
}

function submitGoals(event) {
  event.preventDefault();
  state.goals = {
    calories: Number($("#goalCaloriesInput").value), protein: Number($("#goalProteinInput").value), carbs: Number($("#goalCarbsInput").value),
    fat: Number($("#goalFatInput").value), sugar: Number($("#goalSugarInput").value), water: Number($("#goalWaterInput").value)
  };
  localStorage.setItem("nourish-goals", JSON.stringify(state.goals));
  $("#goalsDialog").close(); render(); showToast(t("goalsSaved"));
}

function changeSection(section) {
  $$(".page-section").forEach(el => el.classList.toggle("active", el.id === `${section}Section`));
  $$('[data-section]').forEach(el => el.classList.toggle("active", el.dataset.section === section));
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function showToast(message) {
  const toast = $("#toast"); toast.textContent = message; toast.classList.add("show");
  clearTimeout(showToast.timeout); showToast.timeout = setTimeout(() => toast.classList.remove("show"), 2300);
}

function escapeHtml(value) {
  const div = document.createElement("div"); div.textContent = value; return div.innerHTML;
}

function bindEvents() {
  $("#languageSelect").addEventListener("change", e => { state.lang = e.target.value; updateLanguage(); });
  $("#themeToggle").addEventListener("click", () => { state.theme = state.theme === "dark" ? "light" : "dark"; updateTheme(); });
  [$("#addFoodTop"), $("#mobileAdd")].forEach(btn => btn.addEventListener("click", () => openFoodDialog()));
  [$("#openGoalsBtn"), $("#mobileGoals"), $("#calorieSettings")].forEach(btn => btn.addEventListener("click", openGoals));
  $("#foodForm").addEventListener("submit", submitFood); $("#goalsForm").addEventListener("submit", submitGoals);
  $$(".close-dialog").forEach(btn => btn.addEventListener("click", () => $("#foodDialog").close()));
  $$(".close-goals").forEach(btn => btn.addEventListener("click", () => $("#goalsDialog").close()));
  $("#prevDay").addEventListener("click", () => changeDate(-1)); $("#nextDay").addEventListener("click", () => changeDate(1));
  $("#dateButton").addEventListener("click", () => $("#datePicker").showPicker());
  $("#datePicker").addEventListener("change", e => { state.date = e.target.value; render(); });
  $$(".filter-btn").forEach(btn => btn.addEventListener("click", () => { state.filter = btn.dataset.filter; $$(".filter-btn").forEach(b => b.classList.toggle("active", b === btn)); renderMeals(); }));
  $$("[data-section]").forEach(btn => btn.addEventListener("click", () => changeSection(btn.dataset.section)));
  $$(".back-dashboard").forEach(btn => btn.addEventListener("click", () => changeSection("dashboard")));
  $$(".water-add").forEach(btn => btn.addEventListener("click", () => { currentDay().water = Number((currentDay().water + Number(btn.dataset.amount)).toFixed(2)); saveDays(); renderSummary(); }));
  $("#waterReset").addEventListener("click", () => { currentDay().water = 0; saveDays(); renderSummary(); showToast(t("waterReset")); });
  $("#mealGroups").addEventListener("click", event => {
    const add = event.target.closest(".add-to-meal"); if (add) return openFoodDialog(add.dataset.meal);
    const edit = event.target.closest(".edit-food"); if (edit) return openFoodDialog("breakfast", currentDay().foods.find(f => f.id === edit.dataset.id));
    const remove = event.target.closest(".delete-food");
    if (remove && confirm(t("confirmDelete"))) { currentDay().foods = currentDay().foods.filter(f => f.id !== remove.dataset.id); saveDays(); render(); showToast(t("deleted")); }
  });
}

function changeDate(amount) {
  const date = parseDate(state.date); date.setDate(date.getDate() + amount); state.date = toDateKey(date); render();
}

bindEvents();
updateTheme();
updateLanguage();
